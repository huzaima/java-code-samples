package assignment.pkg1.project;

/**
 *
 * @author my
 */
public class DNode <T> {
////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
    private int key;                    //key would be unique for every node
    private T value;                    //data
    private DNode next, previous;       //next and previous node's references
////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
    DNode()                             //default constructor
    {
        next = previous = null;
    }
////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
    DNode(T v, int k)                   //constructor setting key and value
    {
        key = k;
        value = v;
    }
////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
    public void setKey(int k)           //setting key
    {
        key = k;
    }
////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
    public void setData(T v)           //setting value
    {
        value = v;
    }
////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
    public void setNext(DNode n)        //setting next node (node after current node)
    {
        next = n;
    }
////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
    public void setPrevious(DNode p)    //setting previous node (node before current node)
    {
        previous = p;
    }
////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
    public int getKey()                 //getting key
    {
        return key;
    }
////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
    public T getData()                 //getting value
    {
        return value;
    }
////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
    public DNode getNext()              //getting next node
    {
        return next;
    }
////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
    public DNode getPrevious()          //getting previous node
    {
        return previous;
    }
////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
}
